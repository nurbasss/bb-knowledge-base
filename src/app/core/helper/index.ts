export function generateRandomColor(i: number): string {
  switch (i) {
    case 1:
      return '#ffaaa6';
    case 2:
      return '#a8e6ce';
    case 3:
      return '#b5d3ff';
    case 4:
      return '#ddd5cc';
    case 5:
      return '#fcd1b5';
    case 6:
      return '#dce6dc';
    case 7:
      return '#abced9';
    case 8:
      return '#e997c1';
    case 9:
      return '#c8cfdc';
    case 10:
      return '#8c8cc5';
    default:
      return '#a8e6ce';
  }
}
